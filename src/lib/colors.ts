const HEX_RE = /^#([0-9a-f]{3}|[0-9a-f]{6}|[0-9a-f]{8})$/;

function isBackgroundOnlyValue(value: string): boolean {
  const v = value.trim().toLowerCase();
  return (
    v.startsWith('linear-gradient') ||
    v.startsWith('radial-gradient') ||
    v.startsWith('conic-gradient') ||
    v.startsWith('rgb(') ||
    v.startsWith('rgba(') ||
    v.startsWith('hsl(') ||
    v.startsWith('hsla(') ||
    v.startsWith('url(') ||
    v === 'none' ||
    v === 'transparent' ||
    v.startsWith('var(')
  );
}

export function isValidHexColor(value: string): boolean {
  return typeof value === 'string' && HEX_RE.test(value.trim().toLowerCase());
}

export function normalizeHexColor(value: string): string {
  if (typeof value !== 'string') return value;
  const trimmed = value.trim();
  if (!trimmed) return value;

  const lower = trimmed.toLowerCase();
  if (HEX_RE.test(lower)) {
    const hex = lower.slice(1);
    if (hex.length === 3) {
      return `#${hex[0]}${hex[0]}${hex[1]}${hex[1]}${hex[2]}${hex[2]}`;
    }
    return `#${hex}`;
  }

  // Background-only values must not be coerced into a solid hex.
  if (isBackgroundOnlyValue(lower)) return value;

  if (NAMED_COLORS[lower]) return NAMED_COLORS[lower];

  return value;
}
